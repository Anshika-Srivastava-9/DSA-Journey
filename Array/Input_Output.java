package Array;

public class Input_Output {
  public static void main(String[] args) {
    int arr[]={7,5,3,4,53,65,23,86};
    System.out.println(arr.length);              // length of Array
    for (int i = 0; i <= arr.length; i++) {
     System.out.print(arr[i]+" ");   
    }
  }  
}
